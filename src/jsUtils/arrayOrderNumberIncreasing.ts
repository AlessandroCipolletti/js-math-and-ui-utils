
/**
 * @function arrayOrderNumberIncreasing
 * Utils to sort an array of number increasing
 *
 * @example
 * const myArray = [1, 5, 3, 2]
 * myArray.sort(arrayOrderNumberIncreasing) // ==> [1, 2, 3, 5]
 *
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
const arrayOrderNumberIncreasing = (a: number, b: number): number => a - b


export default arrayOrderNumberIncreasing
