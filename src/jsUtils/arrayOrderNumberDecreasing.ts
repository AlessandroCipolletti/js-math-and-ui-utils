
/**
 * @function arrayOrderNumberDecreasing
 * Utils to sort an array of number decreasing
 *
 * @example
 * const myArray = [1, 5, 3, 2]
 * myArray.sort(arrayOrderNumberIncreasing) // ==> [5, 3, 2, 1]
 *
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
const arrayOrderNumberDecreasing = (a: number, b: number): number => b - a


export default arrayOrderNumberDecreasing
